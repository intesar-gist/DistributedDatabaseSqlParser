/* SELECT GROUP BY */
SELECT FLUGLINIE.LAND, COUNT(*) FROM FLUGLINIE GROUP BY FLUGLINIE.LAND;


/* SELECT WHERE GROUP BY */
SELECT FLUGLINIE.LAND, COUNT(*) FROM FLUGLINIE WHERE (FLUGLINIE.ALLIANZ = 'Star') GROUP BY FLUGLINIE.LAND;


/* SELECT WHERE GROUP BY */
SELECT BUCHUNG.FLC, SUM(BUCHUNG.PREIS) FROM BUCHUNG WHERE (BUCHUNG.MEILEN > 3000) GROUP BY BUCHUNG.FLC;


/* SELECT WHERE NOTHING GROUP BY */
SELECT BUCHUNG.PNR, SUM(BUCHUNG.MEILEN) FROM BUCHUNG WHERE (BUCHUNG.MEILEN < 0) GROUP BY BUCHUNG.PNR;


/* SELECT GROUP BY HAVING */
SELECT BUCHUNG.NACH, SUM(BUCHUNG.MEILEN) FROM BUCHUNG GROUP BY BUCHUNG.NACH;


/* SELECT GROUP BY HAVING WHERE */
SELECT BUCHUNG.VON, SUM(BUCHUNG.PREIS) FROM BUCHUNG WHERE (BUCHUNG.BNR > 20)
GROUP BY BUCHUNG.VON;

